        private void th() {
            int j = 0;
            for (int i = 0; i < listDomains.SelectedItems.Count; i++) {
                try {
                    string domain = new Uri(listDomains.SelectedItems[i].Text).Host.Replace("www.", "");
                    string url = getOUrl().Replace("[域名关键字]", domain);
                    CookieCollection cookies = new CookieCollection();
                    new xkHttp().httpGET(url, ref cookies);
                    EchoHelper.Echo("提交" + domain + "成功！", "相关域", EchoHelper.EchoType.任务信息);
                    j++;
                } catch {
                }
            }
            EchoHelper.Show("恭喜您，本次共成功提交" + j + "个站点的相关域，等待排名上升吧。", EchoHelper.MessageType.提示);
            btnGO.Enabled = true;

        }

